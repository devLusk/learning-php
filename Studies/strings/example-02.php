<?php

$texto = "hello world!";

// strtoupper() - Função para deixar o texto em maiúsculo
echo strtoupper($texto);
echo "<br>";

// strtolower() - Função para deixar o texto em minúsculo
echo strtolower($texto);
echo "<br>";

// ucfirst() - Função para deixar a primeira letra em maiúsculo
echo ucfirst($texto);
echo "<br>";

// ucwords() - Função para deixar a primeira letra de cada palavra em maiúsculo
echo ucwords($texto);